    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (ListaArchivos.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione archivo en la lista");
            return;
        }
        String pathArchivo = ListaArchivos.getSelectedValue().toString();
        System.out.println(ipContacto.get(0));
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Guardar Archivo");
        File f=new File(ListaArchivos.getSelectedValue().toString());
        fileChooser.setSelectedFile(f);
        int seleccion = fileChooser.showSaveDialog(this);
        if (seleccion == JFileChooser.OPEN_DIALOG) {
            File archivo = fileChooser.getSelectedFile();
            try {
                if (archivo.exists()) {
                    if (JOptionPane.showOptionDialog(this, "El archivo Existe, Desea Reemplazarlo?", "Advertencia", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"SI", "NO"}, "SI") == 0) {
                        String path = archivo.getPath();
                        ClienteArchivos clienteArchivo = new ClienteArchivos(pathArchivo, path, ipContacto.get(0),this);
                        return;
                    } else {
                        JOptionPane.showMessageDialog(this, "La Operaci√≥n fue cancelada");
                        return;
                    }
                }
                String path = archivo.getPath();
                ClienteArchivos clienteArchivo = new ClienteArchivos(pathArchivo, path, ipContacto.get(0),this);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

