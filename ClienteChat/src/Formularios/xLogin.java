    private void btnAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptarMouseClicked
        String respuestaAcceso = null;
        try
        {
            for(int i=0;i<=2;i++)
            {
                Socket socket = new Socket("192.168.0.13",7777); // 192.168.1.7
                DataOutputStream out=new DataOutputStream(socket.getOutputStream());
                out.writeUTF(
                        "<|emisor:|>KAccesoServidorK</|emisor:|>"+txtUsuario.getText().trim());

                DataInputStream in=new DataInputStream(socket.getInputStream());
                respuestaAcceso=in.readUTF();
                socket.close();
                if(i==0)
                {
                    Thread.sleep(4000);
                }
            }
        }
        catch(IOException | InterruptedException ex)
        {
            control.MENSAJE(2, ex.getMessage(), "Error");
        }
        
        if(respuestaAcceso.equals("SI"))
        {
            Usuario usuario = new Usuario();
            usuario.setUsuario(txtUsuario.getText().trim());
            MiUsuario miUsuario=new MiUsuario();
            miUsuario.setUsuario(txtUsuario.getText().trim());
            General G=new General();
            G.show();
            dispose();
        }
        else
        {
            control.MENSAJE(1, "El Nombre de Usuario se encuentro en uso:\n Ponga otro Nombre por favor.", "Usuario en Uso");
        }
    }//GEN-LAST:event_btnAceptarMouseClicked

